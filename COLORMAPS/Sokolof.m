function tab=Sokolof(n)

% Default N:=255;
if nargin == 0
    n=255;
end

% Pixelvärdena till RGB tabellen. Ett index per rad.
pix=[0 1 4 24 28 36 48 64 68 76 96 104 132 148 152 180 212 220 228 252 255];
% RGB färgtabellen. En rad per pixel i PIX.
farg=[...
          0   0   0; ...
         48   0  64; ...
         64   0 112; ...
         64   0 224; ...
         48   0 240; ...
          0  80 240; ...
          0 176 240; ...
          0 240 240; ...
         16 240 240; ...
         80 240 224; ...
         80 240  80; ...
         96 240   0; ...
        240 240   0; ...
        240 240  64; ...
        240 232  64; ...
        240 160   0; ...
        240  32   0; ...
        240   0 212; ...
        240   0 160; ...
        240 192 224; ...
        255 255 255  ...
    ];

% Interpolera N pixelvärden i färgtabellen. Skala om tabellen från 0..255
% till 0..1.
tab=interp1(pix, farg, linspace(0, 255, n)) / 255;
